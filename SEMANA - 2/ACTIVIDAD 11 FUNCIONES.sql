CREATE OR REPLACE FUNCTION SUMA (A IN NUMBER, B IN NUMBER)
RETURN NVARCHAR2
IS
    RESULTADO NVARCHAR2(100);
    R NUMBER;
BEGIN
    R := A + B;
    RESULTADO := 'EL RESULTADO DE LA SUMA ES: '||R;
    RETURN RESULTADO;
END SUMA;
/

BEGIN
    DBMS_OUTPUT.PUT_LINE(SUMA(10,20));
END;
/

SELECT SUMA(2,5) FROM DUAL;


--FUNCION QUE CALCULE EL AREA DE UN CIRCULO Y RECIBE COMO PARAMETRO EL RADIO
CREATE OR REPLACE FUNCTION A_CIRCULO (RADIO IN NUMBER)
RETURN NVARCHAR2
IS
    RESULTADO NVARCHAR2(100);
    AREA NUMBER;
BEGIN
    AREA := 3.1416 * POWER(RADIO,2);
    RESULTADO := 'EL AREA DEL CIRCULO ES: '||AREA;
    
    RETURN RESULTADO;
END A_CIRCULO;
