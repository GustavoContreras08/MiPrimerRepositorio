--15.MOSTRAR CUANTOS HOMBRES Y CUANTAS MUJERES HAY
SELECT GENERO, COUNT(ID_ALUMNO) AS TOTAL_GENEROS FROM ALUMNO WHERE GENERO IN('M','F') GROUP BY GENERO;

