--9.CONTAR CUANTOS ALUMNOS HAY EN TOTAL
SELECT COUNT(ID_ALUMNO) AS TOTAL_ALUMNOS FROM ALUMNO;

--10.CONTAR CUNATOS ALUMNOS HAY POR CARRERA
SELECT CARRERA, COUNT(ID_ALUMNO) AS TOTAL_ALUMNOS FROM ALUMNO WHERE CARRERA IN ('SISTEMAS COMPUTACIONALES', 'ARQUITECTURA','CONTABILIDAD','AERONAUTICA','LOGISTICA','AUTOMOTRIZ') GROUP BY CARRERA;

--11.MOSTRAR EL PROMEDIO GENERAL DE LOS ALUMNOS
SELECT ROUND(AVG(PROMEDIO),2) AS PROMEDIO_ALUMNOS FROM ALUMNO;

--12.MEJOR PROMEDIO
SELECT * FROM ALUMNO WHERE PROMEDIO = (SELECT MAX(PROMEDIO) AS PROMEDIO_MAXIMO FROM ALUMNO);

--13.PEOR PROMEDIO
SELECT * FROM ALUMNO WHERE PROMEDIO = (SELECT MIN(PROMEDIO) AS PROMEDIO_MINIMO FROM ALUMNO);

--14.PROMEDIO EDAD DE LOS ALUMNOS DE SISTEMAS
